#include "fractol.h"

double	ft_atob(char *arg)
{
	double (n), (f);
	int (d), (i), (s);
	n = 0;
	f = 0;
	d = 1;
	i = 0;
	s = 1;
	while (arg[i] == ' ' || (arg[i] >= 9 && arg[i] <= 13))
		i++;
	if (arg[i] == '-' || arg[i] == '+')
		if (arg[i++] == '-')
			s *= -1;
	while (arg[i] >= '0' && arg[i] <= '9')
		n = (n * 10) + (arg[i++] - 48);
	if (arg[i++] == '.')
	{
		while (arg[i] >= '0' && arg[i] <= '9' && d < 1000000)
		{
			f = (f * 10) + (arg[i++] - 48);
			d *= 10;
		}
		n += f / d;
	}
	return (n * s);
}
