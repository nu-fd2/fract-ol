#include "fractol.h"

void	is_error(bool s)
{
	if (s)
	{
		write(1, "\033[1;33mTry:\033[0m\n",16);
		write(1, "\033[1;32m./fractol mandelbrot\033[0m\n",32);
		write(1, "\033[1;33mOr:\033[0m\n",15);
		write(1, "\033[1;32m./fractol julia <-0.1> <1.9>\033[0m\n",40);
	}
	else
		write(2, "\033[1;31mError\033[0m\n", 17);
	exit(0);
}
